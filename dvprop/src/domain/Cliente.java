package domain;

public class Cliente {
	private String apellido;
	private String nombre;
	private Integer telefono;
	private String mail;
	
	
	public Cliente(String apellido, String nombre, Integer telefono, String mail) {
		super();
		this.apellido = apellido;
		this.nombre = nombre;
		this.telefono = telefono;
		this.mail = mail;
	}
	
	
	
	
	
	
}
